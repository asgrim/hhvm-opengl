#include "hphp/runtime/ext/extension.h"
#include "hphp/runtime/vm/native-data.h"
#include "../hhvm_opengl.h"

namespace HPHP {

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

const StaticString s_OpenGLVertex("OpenGLVertex");

class OpenGLVertex {
public:
    OpenGLVertex() { }
    ~OpenGLVertex() { sweep(); }

    void sweep() {
    }

    int x;
    int y;
    int z;
};

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////


void HHVM_METHOD(OpenGLVertex, __construct, int x, int y, int z) {
    auto data = Native::data<OpenGLVertex>(this_);
    data->x = x;
    data->y = y;
    data->z = z;
}

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

void HHVMOpenGLExtension::initOpenGLVertexClass() {
    HHVM_ME(OpenGLVertex, __construct);
    Native::registerNativeDataInfo<OpenGLVertex>(s_OpenGLVertex.get());
}

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

} // namespace HPHP